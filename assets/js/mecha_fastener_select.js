function updateSelect() {
  const hardwareSel = document.getElementById('hardware');
  const specSel = document.getElementById('spec');
  const resultSel = document.getElementById('result');
  const dimSel = document.getElementById('dimension');
  const descSel = document.getElementById('description');

  // Dynamically populate dropdown menu
  for (let item in hardware) {
    hardwareSel.options[hardwareSel.options.length] = new Option(item, item);
  }

  hardwareSel.onchange = function(){
    // Empty 'Specification' dropdowns
    specSel.length = 1;
    resultSel.innerHTML = "";
    dimSel.style.display = 'none';
    descSel.innerHTML = "";

    // Dynamically populate 'Specification' dropdown menu
    for (let x in hardware[this.value]["spec"]) {
      specSel.options[specSel.options.length] = new Option(x, x);
    }
  } 

  specSel.onchange = function(){
    const db = hardware[hardwareSel.value]["spec"][this.value];
    dimSel.style.display = 'inline';
    dimSel.src = `../assets/img/${hardware[hardwareSel.value]["dim"]}`;
    descSel.innerHTML = hardware[hardwareSel.value]["eng"]+"<br>"+hardware[hardwareSel.value]["desc"];

    // Dynamically populate 'Data' list
    resultSel.innerHTML = Object.entries(db).map(([key, value]) => `<strong>${key}</strong>: ${value}`).join(', ');
  }
}

/**
 * Toggles the info tooltip visibility
 */
function toggleInfo() {
  const info = document.getElementById('info-content');
  info.style.display = info.style.display === 'none' ? 'block' : 'none';
}

// Initialize dropdown on load
window.onload = updateSelect;